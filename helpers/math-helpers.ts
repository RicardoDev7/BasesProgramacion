export function addTwoNumbers(number1: number, number2: number) : number {
    return number1 + number2;
}
export function sustractTwoNumbers(number1: number, number2: number) : number {
    return number1 - number2
}
export function dividedTwoNumbers(number1: number, number2: number) : number {
    return number1 / number2;
}
export function timesTwoNumbers(number1: number, number2: number) : number {
    return number1 * number2;
}